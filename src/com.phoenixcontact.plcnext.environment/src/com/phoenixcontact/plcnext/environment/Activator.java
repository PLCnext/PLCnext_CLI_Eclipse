package com.phoenixcontact.plcnext.environment;

import org.eclipse.core.runtime.Status;
import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 */
public class Activator extends AbstractUIPlugin {

	// The plug-in ID
	public static final String PLUGIN_ID = "com.phoenixcontact.plcnext.environment"; //$NON-NLS-1$

	// The shared instance
	private static Activator plugin;
	
	/**
	 * The constructor
	 */
	public Activator() {
	}

	@Override
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	@Override
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	/**
	 * Returns the shared instance
	 *
	 * @return the shared instance
	 */
	public static Activator getDefault() {
		return plugin;
	}
	
	/**
	 * logs the message as error in the eclipse workspace log
	 * @param msg
	 * @param e
	 */
	public void logError(String msg, Throwable e)
	{
		if(e != null)
			getLog().log(new Status(Status.ERROR, PLUGIN_ID, msg, e));
		else
			getLog().log(new Status(Status.ERROR, PLUGIN_ID, msg));
	}

}
